public class EscapeCharacters {
    public static void main(String[] args) {
        // we can use escape characters for formatting
        System.out.println("This is line one.\nThis is line two.");
        System.out.println("\tThis is tabbed over.");
        System.out.println("This string has \"double quotes\" in it.");
        System.out.println("This string has \'single quotes\' in it.");
        System.out.println("This string has a backslash \\ in it.");
    }
}
